package CLS;
	public class User {

		private String Name;
		private String Id;
		private String Pass;
		
		private String Mobile;
		
		private String Wage;
		public void setName(String name) {
			this.Name = name;
		}


		public String getName() {
			return Name;
		}
		
		
		public void setId(String id) {
			this.Id = id;
		}


		public String getId() {
			return Id;
		}
		
		
		public void setPass(String pass) {
			this.Pass = pass;
		}

		public String getPass() {
			return Pass;
		}
		
		
		
		public void setMob(String mob) {
			this.Mobile = mob;
		}

		public String getMob() {
			return Mobile;
		}

		
		
		public void setW(String w) {
			this.Wage = w;
		}

		public String getW() {
			return Wage;
		}


		
	}
